// Merge two strings.
#include <stdio.h>

int main()
{
    char a[100],b[100];
    char c[100];
    int i = 0, j = 0;
    scanf("%s %s", a,b);
    printf("\nFirst string: %s", a);
    printf("\nSecond string: %s", b);
    while (a[i] != '\0') 
    {
        c[j] = a[i];
        i++;
        j++;
    }
    i = 0;
    while (b[i] != '\0') 
    {
        c[j] = b[i];
        i++;
        j++;
    }
    c[j] = '\0';
    printf("\nConcatenated string: %s", c);

    return 0;
}
